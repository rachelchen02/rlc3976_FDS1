#Miles to Kilometers
def miles_to_km(miles):
    return miles * 1.6
#kilometers to miles
def km_to_miles(km):
    return km / 1.6

#Fahrenheit to Celsius
def fahrenheit_to_celsius(f):
    return (f-32)/1.8
#Celsius to Fahrenheit
def celsius_to_fahrenheit(c):
    return (c*1.8)+32

#Kilograms to Pounds
def kg_to_lb(kg):
    return kg * 2.205
#Pounds to Kilograms
def lb_to_kg(lb):
    return lb / 2.205